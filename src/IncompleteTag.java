class IncompleteTag {
    private String tagName;
    private String tagValue;

    public IncompleteTag(String tagName, String tagValue) {
        this.tagName = tagName;
        this.tagValue = tagValue;
    }

    public String getTagName() {
        return tagName;
    }

    public String getTagValue() {
        return tagValue;
    }
}
